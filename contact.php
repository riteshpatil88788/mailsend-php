<script>
    $(document).ready(function(){
        $('#contactform').submit(function(e){
            e.preventDefault();
            var name = $('#names').val();
            var email = $('#email').val();
            var number = $('#phone').val();
            
             var message = $('#message').val();
     
           
               $('#nameerror').html
                $('#emailerror').html('');
            $('#phoneerror').html('');
            $('#messageerror').html('');
        
 
            
            var hasError = false;
            
            if(name == ''){
            $('#nameerror').html('name field is required').css('color','red');
       hasError = true;
            }
            if(email == ''){
            $('#emailerror').html('email field is required').css('color','red');
         hasError = true;
            }
          if(number == ''){
                $('#phoneerror').html('Phone field is required').css('color','red');
                hasError = true;
            } else if(!/^\d{10}$/.test(number)) {
                $('#phoneerror').html('Phone number must be 10 digits').css('color','red');
                hasError = true;
            }
           
         
            if(message == ''){
            $('#messageerror').html('message field is required').css('color','red');
           hasError = true;
            }
         
         
            
             if (hasError) {
                return false;
            }

            $.ajax({
                
                url : "contact_mail.php", 
                type: "post",
                data: $('#contactform').serialize(),
                // dataType: "json",
                success: function(data){
                  if(data == 'success'){
                        alert('Form submited successfully.');
                      window.location.href = "http://amcomprotech.com/thankyou.php";
                    }
                    
                }
            });
        });
    });
</script>


